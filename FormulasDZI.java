    public static void arithN(){
        //check if strings are numbers
        if (Methods.numCheck(jTextField2.getText())&&Methods.numCheck(jTextField3.getText())&&Methods.numCheck(jTextField4.getText())){
            //get strings
            String A1 = jTextField2.getText();
            String N = jTextField3.getText();
            String D = jTextField4.getText();
            //parse strings
            double a1 = Double.parseDouble(A1);
            double n = Double.parseDouble(N);
            double d = Double.parseDouble(D);
            //calculation
            double an = a1 + (d*(n-1));
            //output result
            jTextField1.setText(Methods.eRemover(an));
        }
        else {
            Methods.callError("Non-Valid values for Element of Arithmetic Progression entered");
        }
    }//solves element of arithmetic progression
